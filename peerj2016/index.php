                <h1>How Are Topics Born? Understanding the Research Dynamics Preceding the Emergence of New Areas.<br/> @ <a href="https://peerj.com/computer-science/" target="_blank">PeerJ Computer Science</a></h1>
                <h2><a href="http://kmi.open.ac.uk/people/member/angelo-antonio-salatino" target="_blank">Angelo A. Salatino</a>, <a href="http://kmi.open.ac.uk/people/member/francesco-osborne" target="_blank">Francesco Osborne</a>, <a href="http://kmi.open.ac.uk/people/member/enrico-motta" target="_blank">Enrico Motta</a></h2>
                <h3><a href="http://kmi.open.ac.uk" target="_blank">Knowledge Media Institute, The Open University, Milton Keynes, UK</a></h3>
                
                <header class="major">
                    <h2>Abstract</h2>
                </header>
                <p>The ability to recognise new research trends early is strategic for many stakeholders, such as academics, institutional funding bodies, academic publishers and companies. While the state of the art presents several works on the identification of novel research topics, detecting the emergence of a new research area at a very early stage, i.e., when the area has not been even explicitly labelled and is associated with very few publications, is still an open challenge. This limitation hinders the ability of the aforementioned stakeholders to timely react to the emergence of new areas in the research landscape. In this paper, we address this issue by hypothesising the existence of an embryonic stage for research topics and by suggesting that topics in this phase can actually be detected by analysing diachronically the co-occurrence graph of already established topics. To confirm our hypothesis, we performed a study of the dynamics preceding the creation of novel topics. This analysis showed that the emergence of new topics is actually anticipated by a significant increase of the pace of collaboration and density in the co-occurrence graphs of related research areas. These findings are very relevant to a number of research communities and stakeholders. Firstly, they confirm the existence of an embryonic phase in the development of research topics and suggest that it might be possible to perform very early detection of research topics by taking into account the aforementioned dynamics. Secondly, they bring new empirical evidence to related theories in Philosophy of Science. Finally, they suggest that significant new topics tend to emerge in an environment in which previously less interconnected research areas start cross-fertilising. </p>
                <p>
                    <b>Keywords</b>: Scholarly Data, Empirical Study, Research Trend Detection, Topic Emergence Detection, Topic Discovery, Digital Libraries, Ontology, Semantic Web
                </p>
    
